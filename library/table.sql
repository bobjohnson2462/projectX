DROP TABLE IF EXISTS Readers
DROP TABLE IF EXISTS Books
DROP TABLE IF EXISTS Extradition
DROP TABLE IF EXISTS Reserve

CREATE TABLE Readers(
ReaiderID INT IDENTITY(1,1) PRIMARY KEY,
FullName NVARCHAR(255) NOT NULL,
Phone NVARCHAR(100) NULL,
);


CREATE TABLE Books (
  BookID INT IDENTITY PRIMARY KEY,
  Title NVARCHAR(255) NOT NULL,
  Author NVARCHAR(255) NOT NULL,
  Genre NVARCHAR(100),
);


CREATE TABLE Extradition (
  ExtraditionID INT IDENTITY PRIMARY KEY,
  BookID INT NOT NULL,
  ReaderID INT NOT NULL,
  Extradition DATE,
  FOREIGN KEY (BookID) REFERENCES Books(BookID),
  FOREIGN KEY (ReaderID) REFERENCES Readers(ReaderID)
);


CREATE TABLE Reserve (
  ReserveID INT IDENTITY(1,1) PRIMARY KEY,
  BookID INT NOT NULL,
  ReaderID INT NOT NULL,
  ReserveDate DATE DEFAULT GETDATE(),
  Status NVARCHAR(10) DEFAULT 'Active',
  FOREIGN KEY (BookID) REFERENCES Books(BookID),
  FOREIGN KEY (ReaderID) REFERENCES Readers(ReaderID)
);
